        <h2>OziLoadData</h2>
        <p>
            O <strong>OziLoadData</strong> é um plugin criado para tornar o trabalho com formulários muito mais simples e confiável.
            Ele foi desenvolvido para lidar com diferentes tipos de campos — <code>input</code>, <code>radio</code>, <code>checkbox</code>,
            <code>file</code>, <code>select</code> e <code>textarea</code> — aplicando validações automáticas e organizando os dados em um fluxo consistente.
        </p>

        <h3>Principais recursos</h3>
        <ul>
            <li><strong>Simplificação da coleta de dados:</strong> todos os campos são processados de forma padronizada, sem precisar escrever validações repetitivas.</li>
            <li><strong>Validações inteligentes:</strong> suporta atributos como <code>required</code>, <code>disabled</code>, <code>maxlength</code> e <code>minlength</code>, além de regras específicas para e‑mail, arquivos e seleções múltiplas.</li>
            <li><strong>Feedback visual automático:</strong> adiciona classes CSS (<code>form-control-success</code> / <code>form-control-danger</code>) para indicar ao usuário se o campo foi preenchido corretamente.</li>
            <li><strong>Integração com ferramentas modernas:</strong> compatível com editores como <em>CKEditor</em> e componentes de seleção como <em>Select2</em>.</li>
            <li><strong>Preparado para frameworks:</strong> funciona em ambientes simples ou complexos, adaptando-se bem a frameworks modernos como <em>Livewire</em>, <em>Alpine.js</em> e <em>Vue</em>.</li>
            <li><strong>Envio facilitado:</strong> organiza todos os dados em um objeto <code>FormData</code>, pronto para ser enviado via AJAX ou integrado a APIs.</li>
        </ul>

        <p>
            O objetivo do <strong>OziLoadData</strong> é oferecer uma solução <em>gratuita</em>, <em>leve</em> e <em>flexível</em>,
            que ajude desenvolvedores a economizar tempo e manter seus formulários mais organizados, confiáveis e compatíveis com diferentes cenários.
        </p>
    </section>

